package company;

public class Cwh_16_conditionals {

	public static void main(String[] args) {

// 1)  If-else Statement
		
		
// 		i) Not equals
		int age = 6;
		
		if(age != 18)
		{
			System.out.println("You can drive");
		}
		else
		{
			System.out.println("you can not drive");
		}
		
// 		i) greater than 
		
	int age1 = 6;
		
		if(age1 > 18)
		{
			System.out.println("You can drive");
		}
		else
		{
			System.out.println("you can not drive");
		}
		
	}
}
